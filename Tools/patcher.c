// Function to read entire file into memory
unsigned char *read_file(const char *filename, long *size) {
    FILE *f = fopen(filename, "rb");
    if (!f) {
        perror("Error opening file");
        return NULL;
    }

    fseek(f, 0, SEEK_END);
    *size = ftell(f);
    rewind(f);

    unsigned char *buffer = malloc(*size);
    if (!buffer) {
        perror("Memory allocation failed");
        fclose(f);
        return NULL;
    }

    fread(buffer, 1, *size, f);
    fclose(f);
    return buffer;
}

// Write buffer to new file
int write_file(const char *filename, unsigned char *data, long size) {
    FILE *f = fopen(filename, "wb");
    if (!f) {
        perror("Error writing file");
        return 0;
    }
    fwrite(data, 1, size, f);
    fclose(f);
    return 1;
}

// Search for a byte pattern within a buffer
unsigned char *find_pattern(unsigned char *data, long data_len, unsigned char *pattern, size_t pattern_len) {
    for (long i = 0; i <= data_len - (long)pattern_len; i++) {
        if (memcmp(&data[i], pattern, pattern_len) == 0)
            return &data[i];
    }
    return NULL;
}

// Generate new filename by appending _PATCHED before extension
char *make_patched_filename(const char *original) {
    const char *dot = strrchr(original, '.');
    size_t base_len = dot ? (size_t)(dot - original) : strlen(original);
    size_t ext_len = dot ? strlen(dot) : 0;

    size_t new_len = base_len + strlen("_PATCHED") + ext_len + 1;
    char *new_name = malloc(new_len);
    if (!new_name) {
        perror("Memory allocation failed for filename");
        return NULL;
    }

    strncpy(new_name, original, base_len);
    new_name[base_len] = '\0';
    strcat(new_name, "_PATCHED");
    if (dot)
        strcat(new_name, dot);

    return new_name;
}


// Apply a single patch to a file buffer
int apply_patch(uint8_t *data, size_t size, Patch *p) {
    int applied = 0;
    for (size_t i = 0; i + p->original_len <= size; i++) {
        if (memcmp(data + i, p->original, p->original_len) == 0) {
            memcpy(data + i, p->patch, p->patch_len);
            applied = 1;
            break;
        }
    }
    return applied;
}

int main(int argc, char *argv[]) {
    if (argc < 3) {
        printf("Usage: %s <input_file> <patch_index> [patch_index...]\n", argv[0]);
        printf("Example: %s target.bin 0 4 11\n", argv[0]);
        return 1;
    }

    // Load input file
    FILE *f = fopen(argv[1], "rb");
    if (!f) {
        perror("Error opening input file");
        return 1;
    }
    fseek(f, 0, SEEK_END);
    long size = ftell(f);
    fseek(f, 0, SEEK_SET);

    uint8_t *data = malloc(size);
    if (!data) {
        perror("Memory allocation failed");
        fclose(f);
        return 1;
    }
    fread(data, 1, size, f);
    fclose(f);

    // Apply only selected patches
    size_t num_patches = sizeof(patches) / sizeof(patches[0]);
    for (int i = 2; i < argc; i++) {
        int idx = atoi(argv[i]);
        if (idx < 0 || (size_t)idx >= num_patches) {
            printf("Invalid patch index: %d\n", idx);
            continue;
        }

        Patch *p = &patches[idx];
        printf("Applying patch %d: %s... ", idx, p->name);
        if (apply_patch(data, size, p)) {
            printf("OK\n");
        } else {
            printf("Pattern not found.\n");
        }
    }

    // Write output
    FILE *out = fopen("patched_output.bin", "wb");
    if (!out) {
        perror("Error creating output file");
        free(data);
        return 1;
    }
    fwrite(data, 1, size, out);
    fclose(out);

    printf("Patching complete. Output written to patched_output.bin\n");
    free(data);
    return 0;
}